let maxSteps = 64;

let data = [];

data = strings.split("\n").map(line=> line.split(""));
   
let currentCoords = [];

for(let i=0;i<data.length;i++){
    for(let j=0;j<data[i].length;j++){
        if (data[i][j] === "S"){
            currentCoords.push({i:i,j:j});
            data[i][j] = ".";
        }
    }
}

let newCoords = [];
for(let currentStep = 0;currentStep< maxSteps;currentStep++){
    newCoords = [];
    currentCoords.forEach(startCoord =>{
        let availableDirs = getAvailableDirs(startCoord.i,startCoord.j);
        availableDirs.forEach(availableDir =>{
            newCoords.push({i:startCoord.i+availableDir.i,j:startCoord.j + availableDir.j});
        });
    });
    currentCoords = removeDuplicates(newCoords);
}
console.log(`available pots: ${currentCoords.length}`);



function getAvailableDirs(i,j){
    let res = [];
    if(i>0 && data[i-1][j] == "."){
        res.push({i:-1,j:0});
    }
    if(j>0 && data[i][j-1] == "."){
        res.push({i:0,j:-1});
    }
    if(i< data.length-1 && data[i+1][j] == "."){
        res.push({i:1,j:0});
    }
    if(j< data[i].length-1 && data[i][j+1] == "."){
        res.push({i:0,j:1});
    }
    return res;
}




function removeDuplicates (arr) {
    let uniqArr = [];
    for(let f= 0;f<arr.length;f++){
        let push = true;
        for(let s = 0; s < uniqArr.length;s++){
            if(arr[f].i == uniqArr[s].i && arr[f].j == uniqArr[s].j){
                push = false;
                break;
            }
        }
        if(push){
            uniqArr.push(arr[f]);
        }
    }
    return uniqArr;
}
